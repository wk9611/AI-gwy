async function main() {
  console.log('开始导入示例数据...');

  // 清空现有数据
  await prisma.wrongRecord.deleteMany();
  await prisma.examRecord.deleteMany();
  await prisma.question.deleteMany();

  // 批量创建
  for (const q of seedData) {
    await prisma.question.create({ data: q });
  }

  console.log(`成功导入 ${seedData.length} 道示例题目`);

  // 输出统计
  const categories = ['政治理论', '常识判断', '言语理解', '数量关系', '判断推理', '资料分析'];
  for (const cat of categories) {
    const count = await prisma.question.count({ where: { category: cat } });
    console.log(`  ${cat}: ${count} 题`);
  }
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
